    private void salvarCadastratoPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarCadastratoPedidoActionPerformed
        try {
            GestorPedidos gP1 = new GestorPedidos();
            DefaultTableModel dtm = (DefaultTableModel) tableProdutosCadastro.getModel();
            ArrayList<Integer> ids = new ArrayList<>();
            ArrayList<Integer> qtd = new ArrayList<>();
            
            for (int i = 0; i < dtm.getRowCount(); i++) {
                qtd.add(Integer.parseInt(dtm.getValueAt(i, 2).toString()));
                for(int j = 0; j < qtd.get(i); j++){
                    ids.add(Integer.parseInt(dtm.getValueAt(i, 1).toString()));
                }
            }
            
            int[] dados = new int[ids.size()];
            for (int i=0; i < dados.length; i++){
                    dados[i] = ids.get(i).intValue();
            }
            
            if(cpfClientePedidoCampo.getText().equals("") || dados.length == 0) return;
            
            gP1.cadastrarPedidos(cpfClientePedidoCampo.getText(), dados);
            JOptionPane.showMessageDialog(this, "Pedido Cadastrado com sucesso");
        } catch (IOException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_salvarCadastratoPedidoActionPerformed

